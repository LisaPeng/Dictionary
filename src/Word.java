public class Word
{
    String word;
    String meaning;

    public Word(String w,String m)
    {
        word = w;
        meaning = m;
    }

    //getters
    public String getWord()
    {
        return word;
    }
    public String getMeaning()
    {
        return meaning;
    }

    //setters


}
